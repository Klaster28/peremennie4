public class Main {
    public static void main(String[] args) {
        int x = 123456789;
        System.out.println("Значение переменной 'x' с типом данных 'int' равно " + x);
        byte y = 100;
        System.out.println("Значение переменной 'y' стипом данных 'byte' равно " + y);
        short z = 12345;
        System.out.println("Значение переменной 'z' стипом данных 'short' равно " + z);
        long a = 111222333444L;
        System.out.println("Значение переменной 'a' стипом данных 'long' равно " + a);
        double b = -1.1+200;
        System.out.println("Значение переменной 'b' стипом данных 'duble' равно " + b);
        float c = 2.3f;
        System.out.println("Значение переменной 'c' стипом данных 'float' равно " + c);



    }
}